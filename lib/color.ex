defmodule Color do
  defstruct red: 0, blue: 0, green: 0

  def named(name) do
    color = Map.fetch!(%Colors{}, name)
    %Color{ red: Enum.fetch!(color, 0), green: Enum.fetch!(color, 1), blue: Enum.fetch!(color, 2) }
  end

  def to_list(%Color{ red: red, blue: blue, green: green}) do
    [ blue, green, red ]
  end
